from math import *

r = int(input("Enter the radius of the circle:"))

S = pi * pow(float(r), 2)  # Area of ​​a circle
C = 2 * pi * float(r)      # Circumference

print("Area of ​​a circle: ", round(S, 2))
print("Circumference: ", round(C, 2))

